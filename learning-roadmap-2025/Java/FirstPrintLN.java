package Java;

public class FirstPrintLN {
    
    public static void main(String[] args) {

        // Difference of a println or print line
        System.out.println("I'm Gyro Vinz L. Sagal");
        System.out.println("I'm 21 years old.");
        System.out.println("I'm learning Java.");

        System.out.println("\n");

        // Difference of a print or print
        System.out.print("I'm a BSIT student.");
        System.out.print("I study at Mariano Marcos State University.");

        System.out.println("\n");

        // Difference of adding a \n on the text or an escape sequence.
        System.out.print("I'm a BSIT student.\n");
    }
}
